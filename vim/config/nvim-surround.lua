require("nvim-surround").setup({
  surrounds = {
    ['-'] = {
      add = { '<%- ', ' -%>' },
      find = "<%%%-.-%-%%>",
      delete = "^(<%%%- )().-( %-%%>)()",
    },
    ['='] = {
      add = { '<%= ', ' -%>' },
      find = "<%%=.-%-%%>",
      delete = "^(<%%= )().-( %-%%>)()",
    },
    ['#'] = {
      add = { '<#- ', ' -%>' },
      find = "<%%#%-.-%-%%>",
      delete = "^(<%%#%- )().-( %-%%>)()",
    },
  }
})
